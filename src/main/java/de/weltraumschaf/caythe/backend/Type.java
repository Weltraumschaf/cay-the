/**
 * Available types.
 *
 * @since 1.0.0
 */
public enum Type {

    /**
     * Not existing values.
     */
    NIL,
    /**
     * Boolean values.
     */
    BOOL,
    /**
     * Integer number values.
     */
    INT,
    /**
     * Floating point number values.
     */
    FLOAT,
    /**
     * String values.
     */
    STRING;
}
